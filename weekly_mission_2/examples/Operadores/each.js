// Ejemplo 1: for Each para imprimir elementos de una lista
const numbers = [1, 2, 3, 4, 5];
console.log("Ejemplo 1: Imprimiendo los elementos de una lista")
numbers.forEach(num => console.log(num))

// Ejemplo 1.1: for Each para imprimir el cuadrado de los elementos de una lista
const numbers_x2 = [1, 2, 3, 4, 5];
console.log("Ejemplo 1.1: Imprimiendo el cuadrado de los elementos de una lista")
numbers_x2.forEach(num => console.log(num * num))

